#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int		i;

	i = 0;
	if (n == 0)
		return (0);
	while (!(i = *(unsigned char *)s1 - *(unsigned char *)s2) && *s2 && --n)
	{
		s1++;
		s2++;
	}
	return (i);
}
